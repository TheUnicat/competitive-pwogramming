#include <iostream>
using namespace std;
using ll = long long;

ll t, n, d;

int main() {
    cin >> t;
    for (int i = 0; i < t; i++) {
        cin >> n >> d;

        cout << 1 << " ";
        if (d % 3 == 0 || n >= 3) cout << 3 << " ";
        if (d == 5) cout << 5 << " ";
        if (d == 7 || n >= 3) cout << 7 << " ";
        if (d == 9 || (d % 3 == 0 && n >= 3) || n >= 6) cout << 9 << " ";
        cout << endl;
    }
}

